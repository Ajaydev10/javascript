// about the dataTypePrimitive data types in detail
// There are seven primitive data types in JavaScript: 
// Number: Represents both integer and floating-point numbers.
// String: Represents text as a sequence of characters. Strings are immutable.
// Boolean: Represents a logical value, either true or false.
// Undefined: Indicates a variable has been declared but not assigned a value.
// Null: Represents the intentional absence of any object value.
// Symbol: Introduced in ES6, it's a unique and immutable value.
// BigInt: Handles integers larger than the Number type's limit. 
js
let age = 30; // Number
let name = "Alice"; // String
let isActive = true; // Boolean
let address; // Undefined
let favoriteColor = null; // Null
let id = Symbol("uniqueId"); // Symbol
let largeNumber = 1234567890123456789012345678901234567890n; // BigInt
